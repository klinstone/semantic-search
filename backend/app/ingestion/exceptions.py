"""Исключения, которые могут возникнуть при извлечении текста."""


class ParserError(Exception):
    """Базовый класс для всех ошибок извлечения текста."""


class UnsupportedFormatError(ParserError):
    """MIME-тип не поддерживается ни одним парсером."""


class CorruptFileError(ParserError):
    """Файл повреждён или не соответствует заявленному формату."""


class EmptyTextError(ParserError):
    """Файл удалось распарсить, но извлечённый текст пустой.

    Типичный случай — PDF, состоящий из изображений (скан без OCR-слоя).
    """